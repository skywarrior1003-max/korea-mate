/**
 * restaurants.json → places 테이블 upsert SQL 생성기
 * 실행: node scripts/seed-places-restaurants.mjs
 * 출력: scripts/_places_seed.sql  (Supabase CLI로 실행)
 *
 * 금지: 비밀키 하드코딩 금지 / 환경변수 값 출력 금지
 */

import { readFileSync, writeFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dir = dirname(fileURLToPath(import.meta.url));

const restaurants = JSON.parse(
  readFileSync(resolve(__dir, '../public/data/restaurants.json'), 'utf8')
);

// ── 헬퍼 ─────────────────────────────────────────────────────────────────────

function s(val) {
  if (val === null || val === undefined || val === '') return 'NULL';
  return `'${String(val).replace(/'/g, "''")}'`;
}

function toPlaceId(id) {
  // rest-001 → busan_food_001
  return id.replace(/^rest-/, 'busan_food_');
}

function toSource(src) {
  if (src === 'busan-mat-2026') return 'official_public';
  return 'curated_manual';
}

function toCoordQuality(lat, lng) {
  if (lat == null || lng == null) return 'address_only';
  const dec = String(lat).includes('.') ? String(lat).split('.')[1] : '';
  return dec.length <= 2 ? 'district_center' : 'approximate';
}

function toDetailQuality(r) {
  const hasDesc  = !!r.description_ko;
  const hasAddr  = !!r.address_ko;
  const hasPhone = !!(r.phone);
  // image_url = NULL for all 194 records
  if (hasDesc && hasAddr && hasPhone) return 'basic';
  if (hasAddr) return 'minimal';
  return 'invalid';
}

function toTagsArr(tags) {
  if (!tags || tags.length === 0) return "'{}'";
  return `ARRAY[${tags.map(t => `'${String(t).replace(/'/g, "''")}'`).join(', ')}]`;
}

function toExtra(r) {
  const obj = {
    original_id:          r.id,
    reservation_required: r.reservation_required ?? null,
    googleSearchKeyword:  r.googleSearchKeyword   || null,
  };
  return `'${JSON.stringify(obj).replace(/'/g, "''")}'::jsonb`;
}

// ── 변환 ─────────────────────────────────────────────────────────────────────

const COLS = [
  'place_id', 'name', 'name_ko', 'name_en',
  'category', 'subcategory',
  'description', 'description_ko', 'description_en',
  'address', 'road_address', 'district', 'district_ko', 'city',
  'phone', 'lat', 'lng',
  'google_maps_url', 'naver_maps_url', 'naver_search_keyword',
  'image_url', 'images',
  'source', 'source_url', 'source_ref',
  'award', 'price_range',
  'coordinate_quality', 'detail_quality',
  'is_active', 'is_map_usable', 'is_route_usable', 'is_ai_usable',
  'admin_status',
  'tags', 'extra',
];

const rows = restaurants.map(r => {
  const pid        = toPlaceId(r.id);
  const src        = toSource(r.source);
  const cq         = toCoordQuality(r.latitude, r.longitude);
  const dq         = toDetailQuality(r);
  const isMapUsable = cq === 'approximate';
  const isActive   = r.visible !== false;
  const lat        = r.latitude  != null ? r.latitude  : 'NULL';
  const lng        = r.longitude != null ? r.longitude : 'NULL';

  return `  (${[
    s(pid),
    s(r.name_ko),
    s(r.name_ko),
    s(r.name_en     || null),
    "'restaurant'",
    s(r.category_ko || null),
    s(r.description_ko || null),
    s(r.description_ko || null),
    s(r.description_en || null),
    s(r.address_ko  || null),
    'NULL',
    s(r.district_en || null),
    s(r.district_ko || null),
    "'Busan'",
    s(r.phone       || null),
    String(lat),
    String(lng),
    'NULL',
    'NULL',
    s(r.naverSearchKeyword || null),
    s(r.image || null),
    "'[]'::jsonb",
    s(src),
    'NULL',
    s(r.source),
    s(r.award       || null),
    s(r.price_range || null),
    s(cq),
    s(dq),
    isActive    ? 'TRUE' : 'FALSE',
    isMapUsable ? 'TRUE' : 'FALSE',
    'FALSE',
    'TRUE',
    "'approved'",
    toTagsArr(r.tags),
    toExtra(r),
  ].join(', ')})`;
});

const UPDATE_COLS = COLS.filter(c => c !== 'place_id');

const sql = `-- Generated by scripts/seed-places-restaurants.mjs
-- Source: public/data/restaurants.json  (${restaurants.length} records)
-- Target: places table
-- Method: INSERT ... ON CONFLICT (place_id) DO UPDATE (idempotent)
-- IMPORTANT: DO NOT commit this generated SQL file (_places_seed.sql)

INSERT INTO places (${COLS.join(', ')})
VALUES
${rows.join(',\n')}
ON CONFLICT (place_id) DO UPDATE SET
  ${UPDATE_COLS.map(c => `${c} = EXCLUDED.${c}`).join(',\n  ')},
  updated_at = NOW();
`;

const outPath = resolve(__dir, '_places_seed.sql');
writeFileSync(outPath, sql, 'utf8');
console.log(`✅ SQL 생성 완료: ${outPath}`);
console.log(`   레코드 수: ${restaurants.length}`);
